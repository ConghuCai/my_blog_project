package site.conghucai.blogms.model;

public class BlogTitle {
    private String id;

    private String blogname;

    private String shortname;

    private String blogavatar;

    private String giteelogo;

    private String loginback;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getBlogname() {
        return blogname;
    }

    public void setBlogname(String blogname) {
        this.blogname = blogname;
    }

    public String getShortname() {
        return shortname;
    }

    public void setShortname(String shortname) {
        this.shortname = shortname;
    }

    public String getBlogavatar() {
        return blogavatar;
    }

    public void setBlogavatar(String blogavatar) {
        this.blogavatar = blogavatar;
    }

    public String getGiteelogo() {
        return giteelogo;
    }

    public void setGiteelogo(String giteelogo) {
        this.giteelogo = giteelogo;
    }

    public String getLoginback() {
        return loginback;
    }

    public void setLoginback(String loginback) {
        this.loginback = loginback;
    }
}